SELECT     
				JY65100V.KOMMUNENUMMER AS KOMMUNENUMMER, 
				JY65100V.VEJ_KODE AS VEJKODE, 
				JY65100V.HUS_NUMMER AS HUSNUMMER_BOGSTAV, 
				JY65100V.DISTRIKT_TYPE AS DISTRIKTTYPEKODE, 
				QL59300V.TEKST AS DISTRIKTTYPE, 
				JY65100V.DISTRIKT_KODE AS DISTRIKTKODE, 
				CAST(JY65100V.KOMMUNENUMMER AS VARCHAR(3)) + RIGHT('0000' + CONVERT(VARCHAR,  JY65100V.VEJ_KODE),  4) + RTRIM(JY65100V.HUS_NUMMER) AS ADRESSELINK_ID, 
				CAST(JY65100V.KOMMUNENUMMER AS VARCHAR(3)) + CAST(JY65100V.VEJ_KODE AS VARCHAR(4)) + RTRIM(JY65100V.HUS_NUMMER) AS ADRESSE_ID
FROM         
				JY65100V LEFT OUTER JOIN
				QL59300V ON JY65100V.DISTRIKT_TYPE = QL59300V.KODE
WHERE     
				(JY65100V.DISTRIKT_TYPE = 50)